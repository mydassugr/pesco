using System;

namespace pesco
{


	public class PD_Position
	{
		int x;
		int y;
		public int Y {
			get {
				return y;
			}
			set {
				y = value;
			}
		}
		
		
		public int X {
			get {
				return x;
			}
			set {
				x = value;
			}
		}
		
		
		
		public PD_Position ()
		{
		}
		
		public PD_Position (int x, int y)
		{
			X = x;
			Y = y;
		}
	}
}

